  describe("TCU") {
    describe("with tiny arch") {

      implicit val platformConfig =
        PlatformConfig.default.copy(memKind = MemKind.XilinxBlockRAM)
      val gen                = FixedPoint(16.W, 8.BP)
      val arch               = Architecture.tiny
      implicit val axiConfig = axi.Config.Xilinx64
      implicit val layout    = new InstructionLayout(arch)
      val numBytesPerWord    = 8

      it("should not stall at 0xb4d #0", tensil.tags.Verilator) {
        test(
          new AXIWrapperTCU(
            gen,
            layout,
            AXIWrapperTCUOptions(dramAxiConfig = axiConfig)
          )
        )
          .withAnnotations(Seq(VerilatorBackendAnnotation)) { m =>
            m.instruction.setSourceClock(m.clock)

            m.dram0.readAddress.setSinkClock(m.clock)
            m.dram0.readData.setSourceClock(m.clock)
            m.dram0.writeAddress.setSinkClock(m.clock)
            m.dram0.writeData.setSinkClock(m.clock)
            m.dram0.writeResponse.setSourceClock(m.clock)

            m.dram1.readAddress.setSinkClock(m.clock)
            m.dram1.readData.setSourceClock(m.clock)

            m.clock.setTimeout(100000)

            val size    = 0x2a9 + 1
            val address = 0x400
            val stride  = 2

            val threads = new mutable.ArrayBuffer[TesterThreadList]

            val numWeightInVectors0     = 0x48 + 1
            val numWeightInVectors1     = 0x1 + 1
            val weightInVectors1Address = 0x92
            val numDataInVectors        = 0x2ca + 1

            val addressConversionFactor =
              arch.arraySize / m.numScalarsPerWord * numBytesPerWord
            val sizeConversionFactor = arch.arraySize / m.numScalarsPerWord

            def readZeroes(port: DecoupledIO[ReadData], numWords: Int): Unit = {
              port.setSourceClock(m.clock)
              for (i <- 0 until numWords) {
                val last = i == (numWords - 1)
                port.enqueue(ReadData(0.U, last.B))
              }
            }

            def writeZeroes(
                port: DecoupledIO[WriteData],
                numWords: Int
            ): Unit = {
              port.setSinkClock(m.clock)
              for (i <- 0 until numWords) {
                val last = i == (numWords - 1)
                port.expectDequeue(WriteData(0, last))
              }
            }

            m.dram0.writeResponse.valid.poke(true.B)

            threads += fork {
              var len    = numDataInVectors * sizeConversionFactor
              var offset = 0
              while (len > 256) {
                m.dram0.readAddress.expectDequeue(
                  Address(offset, 256 - 1)
                )
                len -= 256
                offset += 256 * numBytesPerWord
              }
              if (len > 0) {
                m.dram0.readAddress.expectDequeue(
                  Address(offset, len - 1)
                )
              }
            }
            threads += fork {
              var len = numDataInVectors * sizeConversionFactor
              while (len > 256) {
                readZeroes(
                  m.dram0.readData,
                  256
                )
                len -= 256
              }
              if (len > 0) {
                readZeroes(
                  m.dram0.readData,
                  len
                )
              }
            }
            threads += fork {
              for (i <- 0 until numWeightInVectors0) {
                m.dram1.readAddress.expectDequeue(
                  Address(
                    i * stride * addressConversionFactor,
                    sizeConversionFactor - 1
                  )
                )
              }
              for (i <- 0 until numWeightInVectors1) {
                m.dram1.readAddress.expectDequeue(
                  Address(
                    (weightInVectors1Address + i * stride) * addressConversionFactor,
                    sizeConversionFactor - 1
                  )
                )
              }
            }
            threads += fork {
              readZeroes(
                m.dram1.readData,
                numWeightInVectors0 * sizeConversionFactor
              )
              readZeroes(
                m.dram1.readData,
                numWeightInVectors1 * sizeConversionFactor
              )
            }
            threads += fork {
              for (i <- 0 until size) {
                m.dram0.writeAddress.expectDequeue(
                  Address(
                    (address + i * stride) * addressConversionFactor,
                    sizeConversionFactor - 1
                  )
                )
              }
            }
            threads += fork {
              for (i <- 0 until size) {
                writeZeroes(m.dram0.writeData, sizeConversionFactor)
              }
            }
            threads += fork {
              // for (i <- 0 until size) {
              //   m.dram0.writeResponse.enqueue(WriteResponse())
              // }
              m.dram0.writeResponse.valid.poke(true.B)
            }

            threads += fork {
              for (instruction <- program) {
                m.instruction.enqueue(Instruction.fromUInt(instruction.U))
              }
            }

            threads.map(_.join())
          }
      }
    }
  }
  describe("TCU") {
    describe("with tiny arch") {
      // implicit val platformConfig =
      //   PlatformConfig.default.copy(memKind = MemKind.ChiselSyncReadMem)
      implicit val platformConfig =
        PlatformConfig.default.copy(memKind = MemKind.XilinxBlockRAM)
      val gen             = FixedPoint(16.W, 8.BP)
      val arch            = Architecture.tiny
      implicit val layout = new InstructionLayout(arch)

      it("should not stall at 0xb4d", tensil.tags.Verilator) {
        test(new TCU(gen, layout))
          .withAnnotations(Seq(VerilatorBackendAnnotation)) { m =>
            m.io.instruction.setSourceClock(m.clock)
            m.io.dram0.dataIn.setSourceClock(m.clock)
            m.io.dram0.dataOut.setSinkClock(m.clock)
            m.io.dram1.dataIn.setSourceClock(m.clock)
            m.io.dram0.control.setSinkClock(m.clock)
            m.io.dram1.control.setSinkClock(m.clock)

            m.clock.setTimeout(100000)

            val size    = 0x2a9 + 1
            val address = 0x400
            val stride  = 2

            val threads = new mutable.ArrayBuffer[TesterThreadList]

            val numWeightInVectors0     = 0x48 + 1
            val numWeightInVectors1     = 0x1 + 1
            val weightInVectors1Address = 0x92
            val numDataInVectors        = 0x2ca + 1

            threads += fork {
              for (i <- 0 until numWeightInVectors0)
                m.io.dram1.control.expectDequeue(
                  MemControl(
                    arch.dram1Depth,
                    (i * stride).U,
                    0.U,
                    false.B
                  )
                )
              for (i <- 0 until numWeightInVectors1)
                m.io.dram1.control.expectDequeue(
                  MemControl(
                    arch.dram1Depth,
                    (weightInVectors1Address + i * stride).U,
                    0.U,
                    false.B
                  )
                )
            }

            threads += fork {
              for (_ <- 0 until numWeightInVectors0)
                m.io.dram1.dataIn
                  .enqueue(Array.fill(arch.arraySize)(0.F(16.W, 8.BP)))
              for (_ <- 0 until numWeightInVectors1)
                m.io.dram1.dataIn
                  .enqueue(Array.fill(arch.arraySize)(0.F(16.W, 8.BP)))
            }

            threads += fork {
              m.io.dram0.control.expectDequeue(
                MemControl(
                  arch.dram0Depth,
                  0.U,
                  (numDataInVectors - 1).U,
                  false.B
                )
              )
              for (i <- 0 until size) {
                m.io.dram0.control.expectDequeue(
                  MemControl(
                    arch.dram0Depth,
                    (address + i * stride).U,
                    0.U,
                    true.B
                  )
                )
              }
            }
            threads += fork {
              for (_ <- 0 until numDataInVectors) {
                m.io.dram0.dataIn
                  .enqueue(Array.fill(arch.arraySize)(0.F(16.W, 8.BP)))
              }
            }
            threads += fork {
              for (_ <- 0 until size) {
                m.io.dram0.dataOut
                  .expectDequeue(Array.fill(arch.arraySize)(0.F(16.W, 8.BP)))
              }
            }

            threads += fork {
              for (instruction <- program) {
                m.io.instruction.enqueue(Instruction.fromUInt(instruction.U))
              }
            }

            threads.map(_.join())
          }
      }
    }
  }

